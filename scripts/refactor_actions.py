# ──────────────────────────────────────────────────────────────────────────
def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--dry", action="store_true", help="only print stats, do not write")
    args = ap.parse_args()

    json_paths = sorted(p for p in KB.glob("0?-*.json"))
    entries_by_id: dict[str, dict] = {}
    entries_path: dict[str, Path] = {}
    for p in json_paths:
        for e in json.load(p.open(encoding="utf-8")):
            entries_by_id[e["id"]] = e
            entries_path[e["id"]] = p

    missing = [k for k in ACTIONS if k not in entries_by_id]
    extra = [k for k in entries_by_id if k not in ACTIONS]
    if missing:
        raise SystemExit(f"✗ ACTIONS missing entries: {missing}")
    if extra:
        raise SystemExit(f"✗ Entries without ACTIONS: {extra}")

    # Stats
    n_actions = [len(a) for a in ACTIONS.values()]
    n_min, n_max = min(n_actions), max(n_actions)
    avg = sum(n_actions) / len(n_actions)

    when_counts: Counter = Counter()
    for actions in ACTIONS.values():
        for a in actions:
            when_counts[a["when"]] += 1

    if not args.dry:
        for path in json_paths:
            entries = json.load(path.open(encoding="utf-8"))
            for e in entries:
                e["interventions"] = ACTIONS[e["id"]]
            with path.open("w", encoding="utf-8") as f:
                json.dump(entries, f, ensure_ascii=False, indent=2)
            print(f"  ✓ wrote {path.name}")

    print(f"\n[actions per entry]  min={n_min}  max={n_max}  avg={avg:.1f}")
    print(f"[when distribution]  {dict(when_counts)}")
    print(f"[total actions]      {sum(n_actions)}")


if __name__ == "__main__":
    main()
